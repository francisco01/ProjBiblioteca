
public abstract class Comandos {
	
	public String codliv;
	public String codusu;
	public String getCodliv() {
		return codliv;
	}
	public void setCodliv(String codliv) {
		this.codliv = codliv;
	}
	public String getCodusu() {
		return codusu;
	}
	public void setCodusu(String codusu) {
		this.codusu = codusu;
	}
	
	

}
